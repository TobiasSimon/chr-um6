#ifndef __LOCK_H__
#define __LOCK_H__


typedef struct
{
   void *(*create)(void);
   int (*acquire)(void *data);
   int (*release)(void *data);
}
lock_interface_t;


typedef struct
{
   void *data;
   lock_interface_t *interface;
}
lock_t;


void lock_init(lock_t *lock, lock_interface_t *interface);

int lock_acquire(lock_t *lock);

int lock_release(lock_t *lock);


#endif /* __LOCK_H__ */

