#include "posix_lock.h"


#include <pthread.h>
#include <malloc.h>


static void *posix_lock_create(void)
{
   void *lock = malloc(sizeof(pthread_mutex_t));
   pthread_mutex_init((pthread_mutex_t *)lock, NULL);
   return lock;
}


void posix_lock_interface_init(lock_interface_t *lock_interface)
{
   lock_interface->create = posix_lock_create;
   lock_interface->acquire = (int (*)(void *))pthread_mutex_lock;
   lock_interface->release = (int (*)(void *))pthread_mutex_unlock;
}


